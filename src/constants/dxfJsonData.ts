export interface DxfBlockDef {
  name: string
  base_point: DxfPoint
  entity_types: string[]
  entity_count: number
}
 
/* ══════════════════════════════════════════════════════════════════════════
   ROOT DOCUMENT
   ══════════════════════════════════════════════════════════════════════════ */
 
export interface DxfJsonDocument {
  source_file: string
  meta: {
    acad_version: string
    extmin: [number, number, number]
    extmax: [number, number, number]
    insunits?: number
    measurement?: string
  }
  stats: {
    entity_counts: Record<string, number>
    polyline_count: number
    line_count: number
    arc_count: number
    text_count: number
    total_vertex_count: number
    insert_count?: number
    door_insert_count?: number
    window_insert_count?: number
    furniture_insert_count?: number
    stair_insert_count?: number
    hatch_count?: number
    dimension_count?: number
    spline_count?: number
    image_count?: number
    leader_count?: number
  }
 
  layers?: DxfLayerDef[]
  block_defs?: DxfBlockDef[]
 
  lines:      DxfLine[]
  arcs:       DxfArc[]
  circles?:   DxfCircle[]
  ellipses?:  DxfEllipse[]
  splines?:   DxfSpline[]
  points?:    DxfPoint_Entity[]
  polylines:  DxfPolyline[]
 
  texts:      DxfText[]
 
  dimensions?:  DxfDimension[]
  leaders?:     DxfLeader[]
  mleaders?:    DxfMLeader[]
  tolerances?:  DxfTolerance[]
 
  hatches?:   DxfHatch[]
  solids?:    DxfSolid[]
 
  images?:    DxfImage[]
  wipeouts?:  DxfWipeout[]
 
  faces3d?:   DxfFace3d[]
  meshes?:    DxfMesh[]
  helices?:   DxfHelix[]
 
  inserts:            DxfInsert[]
  door_inserts:       DxfInsert[]
  window_inserts:     DxfInsert[]
  furniture_inserts:  DxfInsert[]
  stair_inserts:      DxfInsert[]
 
  window_lines?:    DxfLine[]
  door_lines?:      DxfLine[]
  furniture_lines?: DxfLine[]
 
  raw_entities?: DxfRawEntity[]
}


