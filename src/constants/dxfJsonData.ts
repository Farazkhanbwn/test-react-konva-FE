function _convertCompact(raw: any): DxfJsonDocument {
  const parsedLayers = _parseLayers(raw.layers as string[])

  // v5 stores all entities under raw.data (Python Canvas.to_dict())
  const data = (raw.data ?? raw.msp ?? {}) as Record<string, any>

  // ── Text-height normalisation ─────────────────────────────────────────────
  // v5 compact stores raw DXF text heights (e.g. 50, 200 drawing-units).
  // The canvas rendering formula  fs = height * t.sc * 1.8  was calibrated for
  // metre-scale drawings where height ≈ 0.2.  Divide by (bboxExtent / 60) so a
  // typical DXF text (~50 units on a ~1100-unit drawing) maps to ≈ 2.7 units,
  // yielding a readable ~8-10 px at default zoom.
  const _rawBbox = raw.meta?.bbox
  const _bboxW   = _rawBbox ? Math.abs((_rawBbox.max[0] as number) - (_rawBbox.min[0] as number)) : 1000
  const _bboxH   = _rawBbox ? Math.abs((_rawBbox.max[1] as number) - (_rawBbox.min[1] as number)) : 1000
  const _bboxExt = Math.max(_bboxW, _bboxH, 1)
  const textNormFactor = _bboxExt / 60   // ~18.2 for the villa drawing

  // ── Lines ──────────────────────────────────────────────────────────────────
  const lines: DxfLine[] = []
  if (Array.isArray(data.LN)) {
    _parseLNFlat(data.LN as number[], parsedLayers).forEach((l, i) => {
      lines.push({
        entity_type: 'LINE',
        handle: `L${i}`,
        layer: l.layer,
        start: { x: l.x1, y: l.y1, z: 0 },
        end:   { x: l.x2, y: l.y2, z: 0 },
      })
    })
  }

  // ── Dimension lines (DM) — same stride-5 format as LN ─────────────────────
  if (Array.isArray(data.DM)) {
    _parseLNFlat(data.DM as number[], parsedLayers).forEach((l, i) => {
      lines.push({
        entity_type: 'LINE',
        handle: `DM${i}`,
        layer: l.layer,
        start: { x: l.x1, y: l.y1, z: 0 },
        end:   { x: l.x2, y: l.y2, z: 0 },
      })
    })
  }

  // ── Polylines (LW) — v5 interleaved-bulge format ──────────────────────────
  const polylines: DxfPolyline[] = []
  if (Array.isArray(data.LW)) {
    ;(data.LW as number[][]).forEach((entry, i) => {
      const { layer, closed, verts } = _parseLWEntryV5(entry, parsedLayers)
      polylines.push({
        entity_type: 'LWPOLYLINE',
        handle: `P${i}`,
        layer,
        closed,
        vertex_count: verts.length,
        vertices: verts,
      })
    })
  }

  // ── Circles (CI) — stride 4 ────────────────────────────────────────────────
  const circles: DxfCircle[] = []
  if (Array.isArray(data.CI)) {
    _parseCIFlat(data.CI as number[], parsedLayers).forEach((c, i) => {
      circles.push({
        entity_type: 'CIRCLE',
        handle: `C${i}`,
        layer: c.layer,
        center: { x: c.cx, y: c.cy, z: 0 },
        radius: c.r,
      })
    })
  }

  // ── Arcs (AR) — stride 6 ──────────────────────────────────────────────────
  const arcs: DxfArc[] = []
  if (Array.isArray(data.AR)) {
    _parseARFlat(data.AR as number[], parsedLayers).forEach((a, i) => {
      arcs.push({
        entity_type: 'ARC',
        handle: `A${i}`,
        layer: a.layer,
        center: { x: a.cx, y: a.cy, z: 0 },
        radius: a.r,
        start_angle: a.sa,
        end_angle: a.ea,
      })
    })
  }

  // ── Ellipses (EL) ─────────────────────────────────────────────────────────
  const ellipses: DxfEllipse[] = []
  if (Array.isArray(data.EL)) {
    ;(data.EL as number[][]).forEach(entry => {
      ellipses.push(_parseELEntryV5(entry, parsedLayers))
    })
  }

  // ── Splines (SP) — [layerIdx, [x,y,...]] ──────────────────────────────────
  const splines: DxfSpline[] = []
  if (Array.isArray(data.SP)) {
    ;(data.SP as any[][]).forEach((entry, i) => {
      const { layer, points } = _parseSPEntryV5(entry, parsedLayers)
      if (points.length < 2) return
      splines.push({
        entity_type: 'SPLINE',
        handle: `SP${i}`,
        layer,
        degree: 3,
        closed: false,
        knots: [],
        control_points: [],
        fit_points: [],
        weights: [],
        tessellation: points.map(p => ({ x: p.x, y: p.y, z: 0 })),
      })
    })
  }

  // ── Texts (TX) ────────────────────────────────────────────────────────────
  const texts: DxfText[] = []
  if (Array.isArray(data.TX)) {
    ;(data.TX as any[][]).forEach((entry, i) => {
      const [layerIdx, x, y, height, text] = entry as [number, number, number, number, string]
      // Normalise raw DXF height to canvas-scale; clamp so tiny heights stay visible
      const normH = Math.max(0.5, (height ?? 5) / textNormFactor)
      texts.push({
        entity_type: 'MTEXT',
        handle: `TX${i}`,
        layer: parsedLayers[layerIdx]?.name ?? '0',
        text: String(text ?? ''),
        position: { x, y, z: 0 },
        height: normH,
      })
    })
  }

  // ── Hatches (HT) ──────────────────────────────────────────────────────────
  const hatches: DxfHatch[] = []
  if (Array.isArray(data.HT)) {
    ;(data.HT as any[][]).forEach((entry, i) => {
      hatches.push(_parseHTEntryV5(entry, parsedLayers, i))
    })
  }

  // ── Layer definitions — v5 stores colour as hex in the name string ─────────
  const layerDefs: DxfLayerDef[] = (parsedLayers as any[]).map((l: any) => ({
    name: l.name,
    color: 7,
    true_color: l._hex ?? _aciToHex(7),
    linetype: 'Continuous',
    lineweight: -3,
    plot: true,
    is_frozen: false,
    is_locked: false,
  }))

  // ── Stats ──────────────────────────────────────────────────────────────────
  const totalVerts = polylines.reduce((s, p) => s + p.vertices.length, 0)
  const stats: DxfJsonDocument['stats'] = {
    entity_counts: {
      LINE:       lines.length,
      LWPOLYLINE: polylines.length,
      CIRCLE:     circles.length,
      ARC:        arcs.length,
      ELLIPSE:    ellipses.length,
      SPLINE:     splines.length,
      TEXT:       texts.length,
      HATCH:      hatches.length,
    },
    line_count:             lines.length,
    polyline_count:         polylines.length,
    arc_count:              arcs.length,
    text_count:             texts.length,
    total_vertex_count:     totalVerts,
    insert_count:           0,
    door_insert_count:      0,
    window_insert_count:    0,
    furniture_insert_count: 0,
    stair_insert_count:     0,
    hatch_count:            hatches.length,
    spline_count:           splines.length,
  }

  // ── Extents ────────────────────────────────────────────────────────────────
  const bbox = raw.meta?.bbox
  const extmin: [number, number, number] = bbox?.min ? [bbox.min[0], bbox.min[1], 0] : [0, 0, 0]
  const extmax: [number, number, number] = bbox?.max ? [bbox.max[0], bbox.max[1], 0] : [0, 0, 0]

  return {
    source_file: raw.src ?? 'unknown.dxf',
    meta: {
      acad_version: raw.meta?.acad ?? 'AC1018',
      extmin,
      extmax,
      insunits: raw.meta?.units,
    },
    stats,
    layers: layerDefs,
    lines,
    arcs,
    circles,
    ellipses,
    splines,
    polylines,
    texts,
    hatches,
    dimensions: [],
    leaders: [],
    inserts:            [],
    door_inserts:       [],
    window_inserts:     [],
    furniture_inserts:  [],
    stair_inserts:      [],
  }
}

export const DXF_JSON_DATA: DxfJsonDocument = _convertCompact(_VILLA_COMPACT)